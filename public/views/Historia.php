        En el año 1993,  gracias a su iniciativa, esfuerzo y empeño, el  Sr. César Campoverde Cortéz, decidió trabajar por su propia cuenta haciendo trabajos de soldadura, limpieza mecánica y pequeños trabajos relacionados con la industria petrolera, haciéndose conocido entre las pequeñas empresas de la localidad de Talara.
